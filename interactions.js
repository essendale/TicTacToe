
let lastMove = 0;

function buttonMove1()
{
  if (document.querySelector('.button1').innerHTML === '')
    {
      if (lastMove===0)
        {
          document.querySelector('.button1').innerHTML = 'x';
          lastMove = 1;
          console.log(lastMove);
        }
      else
        {
          document.querySelector('.button1').innerHTML = 'o';
          lastMove = 0;
          console.log(lastMove);
        }console.log(lastMove);
    }
    console.log(lastMove);
}

